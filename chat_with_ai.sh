
#!/bin/bash

echo "Запуск чата с AI помощником для анализа ошибок ОС..."
echo "Убедитесь, что сервер запущен: ./run_server.sh"
echo ""
echo "Откройте в браузере: http://localhost:5000/chat"
echo ""
echo "Возможности чата:"
echo "- Загрузка скриншотов ошибок для автоматического анализа"
echo "- Вопросы о типах ошибок (BSOD, kernel panic, и др.)"
echo "- Получение рекомендаций по устранению проблем"
echo "- Определение типа операционной системы по скриншоту"
echo ""

# Проверить запущен ли сервер
if curl -s http://localhost:5000/ > /dev/null 2>&1; then
    echo "✅ Сервер запущен!"
    echo "Переходите по ссылке: http://localhost:5000/chat"
else
    echo "❌ Сервер не запущен. Запустите его командой: ./run_server.sh"
fi
